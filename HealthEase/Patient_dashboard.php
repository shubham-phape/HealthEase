                    <p>A person with stage 3 chronic kidney disease (CKD) has moderate kidney damage. This stage is broken up into two: a decrease in glomerular filtration rate (GFR) for Stage 3A is 45-59 mL/min and a decrease in GFR for Stage 3B is 30-44 mL/min. As kidney function declines waste products can build up in the blood causing a condition known as uremia. In stage 3 a person is more likely to develop complications of kidney disease such as high blood pressure, anaemia (a shortage of red blood cells) and/or early bone disease. Symptoms may start to become present in stage 3 including:</p>
                    <ul>
                      <li>Fatigue</li>
                      <li>Fluid retention, swelling (edema) of extremities and shortness of breath</li>
                      <li>Urination changes (foamy; dark orange, brown, tea-coloured or red if it contains blood; and urinating more or less than normal)</li>
                      <li>Kidney pain felt in their back</li>
                      <li>Sleep problems due to muscle cramps or restless legs</li>
                    </ul>
