                  <div class="tab-pane" id="tab_4">
                    <p>A person with stage 4 chronic kidney disease (CKD) has advanced kidney damage with a severe decrease in the glomerular filtration rate (GFR) to 15-30 ml/min. It is likely someone with stage 4 CKD will need dialysis or a kidney transplant in the near future. As kidney function declines, waste products build up in the blood causing a condition known as uremia. In stage 4, a person is likely to develop complications of kidney disease such as high blood pressure, anaemia (a shortage of red blood cells), bone disease, heart disease and other cardiovascular diseases. Symptoms that are experienced in stage 4 include:</p>
                    <ul>
                      <li>Fatigue</li>
                      <li>Fluid retention, swelling (edema) of extremities and shortness of breath</li>
                      <li>Urination changes (foamy; dark orange, brown, tea-coloured or red if it contains blood; and urinating more or less than normal)</li>
                      <li>Kidney pain felt in their back</li>
                      <li>Sleep problems due to muscle cramps or restless legs</li>
                      <li>Nausea and/or vomiting</li>
                      <li>Taste changes such as a metallic taste in the mouth</li>
                      <li>Bad breath due to urea build up in the blood</li>
                      <li>Loss of appetite: People may not feel like eating due to nausea or other symptoms caused by waste build up in the blood</li>
                      <li>Difficulty in concentrating: Having trouble doing everyday things such as balancing a check book or focusing on reading the newspaper can occur</li>
                      <li>Nerve problems: Numbness or tingling in the toes or fingers is a symptom of CKD</li>
                    </ul>
                  </div>
